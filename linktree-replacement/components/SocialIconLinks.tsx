interface SocialLink {
    platform: string
    url: string
}

interface SocialIconLinksProps {
    /**
     * Array of social links
     */
    links: SocialLink[]

    /**
     * Icon size in pixels
     * @default 24
     */
    iconSize: number

    /**
     * Gap between icons in pixels
     * @default 15
     */
    gap: number

    /**
     * Icon color
     * @default "#FFFFFF"
     */
    iconColor: string

    /**
     * Icon color on hover
     * @default "rgba(255, 255, 255, 0.7)"
     */
    hoverColor: string

    style?: CSSProperties
}

/**
 * SocialIconLinks - Social Media Icon Bar
 *
 * @framerDisableUnlink
 * @framerIntrinsicWidth 300
 * @framerIntrinsicHeight 24
 *
 * @framerSupportedLayoutWidth any
 * @framerSupportedLayoutHeight any
 */
export default function SocialIconLinks(props: SocialIconLinksProps) {
    const {
        links = [
            { platform: "linkedin", url: "https://linkedin.com" },
            { platform: "instagram", url: "https://instagram.com" },
            { platform: "x", url: "https://x.com" },
        ],
        iconSize = 24,
        gap = 15,
        iconColor = "#FFFFFF",
        hoverColor = "rgba(255, 255, 255, 0.7)",
        style,
    } = props

    const containerStyle: CSSProperties = {
        display: "flex",
        flexDirection: "row",
        alignItems: "center",
        justifyContent: "center",
        gap: gap,
        flexWrap: "wrap",
        ...style,
    }

    const linkStyle: CSSProperties = {
        display: "flex",
        alignItems: "center",
        justifyContent: "center",
        width: iconSize,
        height: iconSize,
        color: iconColor,
        transition: "color 0.2s ease, transform 0.2s ease",
        textDecoration: "none",
    }

    return (
        <div style={containerStyle}>
            {links.map((link, index) => {
                const icon = SOCIAL_ICONS[link.platform.toLowerCase()]
                if (!icon) return null

                return (
                    <a
                        key={index}
                        href={link.url}
                        target="_blank"
                        rel="noopener noreferrer"
                        style={linkStyle}
                        onMouseEnter={(e) => {
                            e.currentTarget.style.color = hoverColor
                            e.currentTarget.style.transform = "scale(1.1)"
                        }}
                        onMouseLeave={(e) => {
                            e.currentTarget.style.color = iconColor
                            e.currentTarget.style.transform = "scale(1)"
                        }}
                        aria-label={link.platform}
                    >
                        {icon}
                    </a>
                )
            })}
        </div>
    )
}

